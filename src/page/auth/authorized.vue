<template></template>

<script>
	import { Indicator} from 'mint-ui'
	import { userProfileGet } from '../../api/network/user'
	import { mapMutations, mapActions, mapState } from 'vuex'
	export default {
		created(){
			this.onAuthSuccess();
		},

		methods: {
			...mapMutations({
				saveUser: 'saveUser',
				saveToken: 'saveToken'
			}),

			onAuthSuccess () {
				let openid = this.$cookie.get('openid')
		  		let token = this.$cookie.get('token')
		  		if (openid && token) {
		  			this.saveToken({ 'token': token })
		  			Indicator.open()
			  		userProfileGet().then(response => {
		  				Indicator.close()
		  				if (response && response.user) {
		  					this.saveUser(response.user);
		  					debugger;
		  					if (response.user.mobile_binded) {
		  						this.goSingIn();
		  					} else {
		  						this.goSignup();
		  					}
		  				}
			  		},error => {
		  				Indicator.close()
		  				this.goHome()
			  		});
		  		}
			},

			goSignup() {
				this.$router.push({'name': 'signup', 'query': {'mode': 'bind'}})
			},

			goSingIn() {
				this.$router.push({'name': 'signin', 'params': {'isTokenInvalid': false}})
			},

			goHome() {
	  			this.$router.push('/home')
	  		}
		}
	}
</script>
